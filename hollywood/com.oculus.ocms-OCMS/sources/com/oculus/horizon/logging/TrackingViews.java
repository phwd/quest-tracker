        public static View fromStringWithDefaults(String str) {
            if (str == null) {
                return NOT_SET;
            }
            try {
                return valueOf(str.toUpperCase(Locale.US));
            } catch (IllegalArgumentException unused) {
                return INVALID;
            }
        }

