package com.oculus.content;

import com.facebook.common.android.AndroidModule;
import com.facebook.inject.AbstractLibraryModule;
import com.facebook.inject.AutoGeneratedBinder;
import com.facebook.inject.Binder;
import com.facebook.inject.BundledAndroidModule;
import com.facebook.inject.InjectorModule;
import com.facebook.ultralight.AutoGeneratedSwitchIdClass;
import com.facebook.ultralight.UL;
import com.oculus.common.init.AppInitModule;
import com.oculus.security.basecomponent.BasecomponentModule;
import com.oculus.util.device.DeviceModule;
import com.oculus.util.thread.ThreadModule;

@InjectorModule
public class ContentModule extends AbstractLibraryModule {

    @AutoGeneratedSwitchIdClass
    public static final class UL_id {
    }

    @AutoGeneratedBinder
    static class AutoGeneratedBindingsForContentModule {
        AutoGeneratedBindingsForContentModule() {
        }

        static void bind(Binder binder) {
            if (!UL.USE_STATIC_DI) {
                binder.require(AndroidModule.class);
                binder.require(BundledAndroidModule.class);
                binder.require(com.oculus.android.AndroidModule.class);
                binder.require(AppInitModule.class);
                binder.require(BasecomponentModule.class);
                binder.require(DeviceModule.class);
                binder.require(ThreadModule.class);
                binder.bindComponent(OculusFbPermissionsContentProvider.class).toProvider(new OculusFbPermissionsContentProviderAutoProvider());
                binder.bindComponent(OculusPublicContentProvider.class).toProvider(new OculusPublicContentProviderAutoProvider());
                binder.bindComponent(OculusSecureContentProvider.class).toProvider(new OculusSecureContentProviderAutoProvider());
            }
        }
    }
}
