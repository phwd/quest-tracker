package com.oculus.appmanager.installer.boot;

import com.facebook.inject.AbstractLibraryModule;
import com.facebook.inject.AutoGeneratedBinder;
import com.facebook.inject.Binder;
import com.facebook.inject.BundledAndroidModule;
import com.facebook.inject.InjectorModule;
import com.facebook.ultralight.AutoGeneratedSwitchIdClass;
import com.facebook.ultralight.UL;
import com.oculus.appmanager.installer.contract.ContractModule;

@InjectorModule
public class BootModule extends AbstractLibraryModule {

    @AutoGeneratedSwitchIdClass
    public static final class UL_id {
    }

    @AutoGeneratedBinder
    static class AutoGeneratedBindingsForBootModule {
        AutoGeneratedBindingsForBootModule() {
        }

        static void bind(Binder binder) {
            if (!UL.USE_STATIC_DI) {
                binder.require(BundledAndroidModule.class);
                binder.require(ContractModule.class);
                binder.bindComponent(BootCompletedAction.class).toProvider(new BootCompletedActionAutoProvider());
            }
        }
    }
}
